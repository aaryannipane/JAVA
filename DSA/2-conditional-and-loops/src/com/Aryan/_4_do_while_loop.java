package com.Aryan;

public class _4_do_while_loop {
    public static void main(String[] args) {
        // do while loop
        /*
            initialization
            do{
                body
                incrementation
            }while(condition);
        */
        int x = 10;
        do {
            System.out.println(x);
            x++;
        }while(x<0);



    }
}
